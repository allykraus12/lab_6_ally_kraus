class Node:
	def __init_(self, data, nextPointer):

		self.data        = data
		self.nextPointer = nextPointer

	def getData():

		return data

	def getNextPointer():

		return nextPointer
		
	def setData(self,data):

		self.data = data

	def setNextPointer(self, nextPointer):

		self.nextPointer = nextPointer




